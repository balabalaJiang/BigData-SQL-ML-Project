def get_next_id(cursor, table, pk_column):
    cursor.execute(f"SELECT MAX({pk_column}) FROM {table}")
    row = cursor.fetchone()
    return (row[0] or 0) + 1

# 初始化：確保 Songs 資料表有 play_count 欄位
def init_db_schema():
    conn = get_db_connection()
    if conn:
        cursor = conn.cursor()
        try:
            # 檢查是否存在 play_count 欄位，若無則建立
            cursor.execute("IF NOT EXISTS (SELECT * FROM sys.columns WHERE object_id = OBJECT_ID('Songs') AND name = 'play_count') BEGIN ALTER TABLE Songs ADD play_count INT DEFAULT 0; END")
            # 確保舊資料的 play_count 不是 NULL
            cursor.execute("UPDATE Songs SET play_count = 0 WHERE play_count IS NULL")
            conn.commit()
        except Exception as e:
            print(f"Schema 更新失敗: {e}")
        finally:
            conn.close()

