@app.route('/edit/<int:id>', methods=['GET', 'POST'])
def edit_song(id):
    conn = get_db_connection()
    cursor = conn.cursor()
    if request.method == 'POST':
        try:
            title = request.form['song_title']
            lang = request.form['song_language']
            al_new = request.form.get('al_new', '').strip()
            al_sel = request.form.get('al_sel')
            a_id = None
            if al_new:
                a_id = get_next_id(cursor, 'Albums', 'album_id')
                cursor.execute("INSERT INTO Albums (album_id, album_title, album_date) VALUES (?, ?, GETDATE())", (a_id, al_new))
            else: a_id = al_sel if al_sel else None

            cursor.execute("UPDATE Songs SET song_title=?, song_language=?, album_id=? WHERE song_id=?", (title, lang, a_id, id))
            cursor.execute("DELETE FROM Song_Creator_Roles WHERE song_id=?", (id,))
            roles = [('歌手','s_s','s_n'), ('作曲','c_s','c_n'), ('作詞','l_s','l_n')]
            for r_name, sel_k, new_k in roles:
                c_id = None
                new_n = request.form.get(new_k, '').strip()
                if new_n:
                    c_id = get_next_id(cursor, 'Creators', 'creator_id')
                    cursor.execute("INSERT INTO Creators (creator_id, creator_name) VALUES (?, ?)", (c_id, new_n))
                else: c_id = request.form.get(sel_k)
                if c_id: cursor.execute("INSERT INTO Song_Creator_Roles (song_id, creator_id, role) VALUES (?, ?, ?)", (id, c_id, r_name))
            conn.commit()
            flash(f'作品 #{id} 修改成功！', 'success')
            return redirect(url_for('index'))
        except Exception as e:
            conn.rollback(); flash(f'修改失敗: {str(e)}', 'danger')

    cursor.execute("SELECT * FROM Songs WHERE song_id=?", (id,))
    song = cursor.fetchone()
    cursor.execute("SELECT album_id, album_title FROM Albums"); als = cursor.fetchall()
    cursor.execute("SELECT creator_id, creator_name FROM Creators"); crs = cursor.fetchall()
    cursor.execute("SELECT role, creator_id FROM Song_Creator_Roles WHERE song_id=?", (id,))
    roles_rows = cursor.fetchall()
    current_roles = {'歌手': None, '作曲': None, '作詞': None}
    for r in roles_rows: current_roles[r.role] = r.creator_id
    conn.close()
    return render_template_string(BASE_TEMPLATE.replace('__CONTENT_BLOCK__', EDIT_SONG_HTML), song=song, albums=als, creators=crs, current_roles=current_roles, active_page='home')

@app.route('/ml')
def ml_analysis():
    conn = get_db_connection()
    cursor = conn.cursor()
    ml_sql = """
EXEC sp_execute_external_script @language = N'Python', @script = N'
import pandas as pd
from sklearn.cluster import KMeans
df = InputDataSet
features = df[["singer_ratio", "composer_ratio", "lyricist_ratio"]]
kmeans = KMeans(n_clusters=3, random_state=42, n_init=10)
kmeans.fit(features)
df["cluster_id"] = kmeans.labels_
def get_p(c):
    if c == 0: return "純粹歌者"
    elif c == 1: return "幕後創作人"
    else: return "全能創作歌手"
df["marketing_persona"] = df["cluster_id"].apply(get_p)
OutputDataSet = df[["creator_name", "singer_ratio", "composer_ratio", "lyricist_ratio", "cluster_id", "marketing_persona"]]
', @input_data_1 = N'
SELECT c.creator_name, CAST(SUM(CASE WHEN scr.role = ''歌手'' THEN 1 ELSE 0 END) AS FLOAT) / COUNT(*) AS singer_ratio,
CAST(SUM(CASE WHEN scr.role = ''作曲'' THEN 1 ELSE 0 END) AS FLOAT) / COUNT(*) AS composer_ratio,
CAST(SUM(CASE WHEN scr.role = ''作詞'' THEN 1 ELSE 0 END) AS FLOAT) / COUNT(*) AS lyricist_ratio
FROM Song_Creator_Roles scr JOIN Creators c ON scr.creator_id = c.creator_id
GROUP BY c.creator_name HAVING COUNT(*) > 0'
WITH RESULT SETS ((creator_name NVARCHAR(255), singer_ratio FLOAT, composer_ratio FLOAT, lyricist_ratio FLOAT, cluster_id INT, marketing_persona NVARCHAR(100)));
"""
    try:
        cursor.execute(ml_sql); results = cursor.fetchall()
        counts = {'vocalist': 0, 'bts': 0, 'allround': 0}
        for r in results:
            if r.cluster_id == 0: counts['vocalist'] += 1
            elif r.cluster_id == 1: counts['bts'] += 1
            else: counts['allround'] += 1
    except Exception as e: flash(f"AI 分析失敗: {str(e)}", "danger"); results, counts = [], {'vocalist': 0, 'bts': 0, 'allround': 0}
    conn.close()
    return render_template_string(BASE_TEMPLATE.replace('__CONTENT_BLOCK__', ML_HTML), ml_results=results, persona_counts=counts, active_page='ml')

